import java.util.Arrays;

public class arrays {
    public static void main(String[] args){
        int [][] mynum = {{1, 4, 6, 8}, {5, 7, 8, 9}};
        for (int i = 0; i < mynum.length; ++i) {
            for (int j = 0; j<mynum[i].length; ++j) {
                System.out.print(mynum[i][j]);

            }
        }
    }
}

